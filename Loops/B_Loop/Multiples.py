# Write a function `five_multiples_of(n)` that prints the first five multiples of n.
# The function does not return a value, just prints.




def five_multiples_of(n):
    for num in range(1, 6):
        print(n * num)
    
five_multiples_of(7)
print("*******")
five_multiples_of(5)
# Example:


# 7
# 14
# 21
# 28
# 35
